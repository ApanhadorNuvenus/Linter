class YoutubeRepositoryImpl(
    private val mlKitTranslator: TextTranslator,
    private val onnxTranslator: TextTranslator,
    context: Context // НОВОЕ
) : YoutubeRepository {

    private val videoBox get() = ObjectBox.store.boxFor(YoutubeVideoEntity::class.java)
    private val subtitleBox get() = ObjectBox.store.boxFor(SubtitleBlockEntity::class.java)
    private val prefs = context.getSharedPreferences("youtube_prefs", Context.MODE_PRIVATE)

    private val TAG = "LINTER_DEBUG"


    companion object {
        // Pre-compiling Regex to avoid compilation inside loop structures
        private val UNWANTED_CHARACTERS_REGEX = Regex("[^\\p{L}']")
        private val ENDS_WITH_PUNCTUATION_REGEX = Regex(".*[.!?,]$")
        private val XML_TEXT_TAG_PATTERN = java.util.regex.Pattern.compile("<text start=\"([\\d.]+)\"(?: dur=\"([\\d.]+)\")?[^>]*>(.*?)</text>", java.util.regex.Pattern.DOTALL)
        private val CLEAN_HTML_TAGS_REGEX = Regex("<[^>]*>")
        private val MULTIPLE_SPACES_REGEX = Regex("\\s+")
        private val RUSSIAN_PUNCTUATION_REGEX = Regex("[^а-яё]")
        private val SENTENCE_END_MATCH_REGEX = Regex(".*[,.!?;:][\"'\\]»]*$")
        private val VIDEO_ID_EXTRACTION_REGEX = Regex("(?:v=|youtu\\.be/|embed/|shorts/)([\\w-]{11})")
    }

    override fun getDefaultQuality(): String = prefs.getString("default_quality", "Auto") ?: "Auto"
    override fun setDefaultQuality(quality: String) = prefs.edit().putString("default_quality", quality).apply()

    override suspend fun deleteSubtitlesForVideo(videoId: Long) {
        withContext(Dispatchers.IO) {
            val blocksToRemove = subtitleBox.query(SubtitleBlockEntity_.youtubeVideoId.equal(videoId)).build().find()
            subtitleBox.remove(blocksToRemove)
        }
    }

    override suspend fun extractAndSaveVideo(url: String): Result<YoutubeVideo> = withContext(Dispatchers.IO) {
        try {
            val info = StreamInfo.getInfo(url)
            val entity = YoutubeVideoEntity(
                videoUrl = info.url,
                title = info.name,
                thumbnailUrl = info.thumbnails.firstOrNull()?.url ?: "",
                durationMs = info.duration * 1000L
            )
            val existing = videoBox.all.find { it.videoUrl == entity.videoUrl }
            val id = existing?.id ?: videoBox.put(entity)
            entity.id = id
            Result.success(entity.toDomain())
        } catch (e: Exception) {
            Result.failure(e)
        }
    }

    override suspend fun getSavedVideos(): List<YoutubeVideo> = withContext(Dispatchers.IO) { videoBox.all.map { it.toDomain() } }
    override suspend fun getVideoById(id: Long): YoutubeVideo? = withContext(Dispatchers.IO) { videoBox[id]?.toDomain() }

    override suspend fun updateProgress(id: Long, progressMs: Long) {
        withContext(Dispatchers.IO) {
            val entity = videoBox[id] ?: return@withContext
            entity.progressMs = progressMs
            videoBox.put(entity)
        }
    }

    override suspend fun deleteVideo(id: Long) {
        withContext(Dispatchers.IO) {
            deleteSubtitlesForVideo(id)
            videoBox.remove(id)
        }
    }

    override suspend fun fetchPlaybackInfo(videoId: Long, url: String, targetQuality: String): Result<VideoPlaybackInfo> = withContext(Dispatchers.IO) {
        try {
            Log.i(TAG, "🎬 [SUBTITLE_SOURCE] ЗАПУСК ВИДЕО ID: $videoId | Качество: $targetQuality")

            val info = StreamInfo.getInfo(url)
            val ytVideoId = extractVideoId(url) ?: throw Exception("Не удалось извлечь videoId")

            // =================================================================
            // 1. ОПРЕДЕЛЯЕМ ИСТИННЫЙ ОРИГИНАЛЬНЫЙ ЯЗЫК ВИДЕО
            // =================================================================
            var sourceLang = "en"
            var sourceUrl: String? = null

            val autoSub = info.subtitles.orEmpty().find { it.isAutoGenerated }
            if (autoSub != null) {
                sourceLang = autoSub.languageTag.substringBefore("-")
                sourceUrl = autoSub.url
            } else {
                val innertubeAsr = getInnertubeCaptionInfo(ytVideoId, targetLang = null)
                if (innertubeAsr != null) {
                    sourceUrl = innertubeAsr.first
                    sourceLang = innertubeAsr.second
                } else {
                    val audioLang = info.audioStreams.orEmpty().firstOrNull { it.audioLocale != null }?.audioLocale?.language
                    if (audioLang != null) sourceLang = audioLang
                }
            }

            val manualSub = info.subtitles.orEmpty().find { !it.isAutoGenerated && it.languageTag.startsWith(sourceLang, ignoreCase = true) }
            if (manualSub != null) {
                sourceUrl = manualSub.url
                sourceLang = manualSub.languageTag.substringBefore("-")
            } else if (sourceUrl == null) {
                val fallbackSub = info.subtitles.orEmpty().firstOrNull() ?: throw Exception("Субтитры отсутствуют полностью.")
                sourceUrl = fallbackSub.url
                sourceLang = fallbackSub.languageTag.substringBefore("-")
            }

            val finalSourceUrl = sourceUrl ?: throw Exception("Ссылка на субтитры пуста")

            // =================================================================
            // 2. БЕЗОПАСНЫЙ ВЫБОР АУДИО И ВИДЕО (ЗАЩИТА ОТ КРАШЕЙ)
            // =================================================================
            val allAudioStreams = info.audioStreams.orEmpty()
            val hasSeparateAudio = allAudioStreams.isNotEmpty()

            // Если отдельных аудиодорожек нет, мы ОБЯЗАНЫ брать видео со встроенным звуком (videoStreams),
            // иначе получим немое кино, взяв поток из videoOnlyStreams.
            val allVideoStreams = if (hasSeparateAudio) {
                info.videoOnlyStreams.orEmpty() + info.videoStreams.orEmpty()
            } else {
                info.videoStreams.orEmpty()
            }

            if (allVideoStreams.isEmpty()) throw Exception("Не найдены видеопотоки")

            val availableQualities = listOf("Auto") + allVideoStreams
                .mapNotNull { it.resolution }
                .distinct()
                .sortedByDescending { it.replace("p", "").toIntOrNull() ?: 0 }

            val targetRes = if (targetQuality == "Auto") "720p" else targetQuality

            val videoStream = allVideoStreams.filter { it.resolution == targetRes }.maxByOrNull { it.bitrate }
                ?: allVideoStreams.filter { it.resolution == "720p" }.maxByOrNull { it.bitrate }
                ?: allVideoStreams.maxByOrNull { it.resolution?.replace("p", "")?.toIntOrNull() ?: 0 }
                ?: allVideoStreams.first()

            val audioStream = allAudioStreams.filter { it.audioLocale?.language?.startsWith(sourceLang, true) == true }.maxByOrNull { it.averageBitrate }
                ?: allAudioStreams.filter { it.audioLocale == null }.maxByOrNull { it.averageBitrate }
                ?: allAudioStreams.maxByOrNull { it.averageBitrate }

            // БЕЗОПАСНЫЙ ФОЛБЭК: Если аудиопоток не найден, берем ссылку на видео (в нем уже есть встроенный звук)
            val finalAudioUrl = audioStream?.url ?: videoStream.url
            Log.i(TAG, "🎙️ [SUBTITLE_SOURCE] Выбран аудиопоток: ${audioStream?.audioLocale?.language ?: "Встроенный в видео"}")

            // =================================================================
            // 3. ПРОВЕРКА КЭША
            // =================================================================
            val cachedEntities = subtitleBox.query(SubtitleBlockEntity_.youtubeVideoId.equal(videoId)).build().find()
            if (cachedEntities.isNotEmpty()) {
                val domainBlocks = cachedEntities.map {
                    SubtitleBlock(it.id, it.startTimeMs, it.endTimeMs, it.sourceText, it.translatedText, it.mlKitTranslatedText, it.onnxTranslatedText)
                }.sortedBy { it.startTimeMs }

                // ИСПРАВЛЕНИЕ: Проверяем, действительно ли в кэше есть переводы от YouTube (translatedText)
                val hasYtTranslation = domainBlocks.any { !it.translatedText.isNullOrBlank() }

                return@withContext Result.success(VideoPlaybackInfo(
                    videoStream.url.toString(),
                    finalAudioUrl.toString(),
                    domainBlocks,
                    hasYtTranslation, // <-- Теперь здесь динамическая проверка, а не жесткий true
                    sourceLang,
                    availableQualities
                ))
            }

            // =================================================================
            // 4. СКАЧИВАНИЕ И СМАРТ-ЧАНКИНГ ОРИГИНАЛА
            // =================================================================
            val rawSourceBlocks = fetchAndParseBlocks(finalSourceUrl)
            if (rawSourceBlocks.isEmpty()) throw Exception("Не удалось распарсить субтитры.")
            val smartSourceBlocks = buildAestheticSubtitles(rawSourceBlocks)

            // =================================================================
            // 5. ПОЛУЧЕНИЕ ПЕРЕВОДА (RU)
            // =================================================================
            var ruBlocks = emptyList<SubtitleBlock>()
            try {
                if (sourceLang.startsWith("ru", true)) {
                    ruBlocks = smartSourceBlocks
                } else {
                    val nativeRuUrl = info.subtitles.orEmpty().find { !it.isAutoGenerated && it.languageTag.startsWith("ru", ignoreCase = true) }?.url
                    if (nativeRuUrl != null) {
                        ruBlocks = fetchAndParseBlocks(nativeRuUrl)
                    } else {
                        val ruUrl = if (finalSourceUrl.contains("tlang=")) finalSourceUrl else "$finalSourceUrl&tlang=ru"
                        try {
                            ruBlocks = fetchAndParseBlocks(ruUrl)
                        } catch (e: Exception) {
                            val innertubeRuUrl = getInnertubeCaptionInfo(ytVideoId, targetLang = "ru")?.first
                            if (innertubeRuUrl != null) ruBlocks = fetchAndParseBlocks(innertubeRuUrl)
                        }
                    }
                }
            } catch (e: Exception) {}

            // =================================================================
            // 6. СЛИЯНИЕ И СОХРАНЕНИЕ
            // =================================================================
            val mergedBlocks = if (ruBlocks.isNotEmpty()) mergeSubtitles(smartSourceBlocks, ruBlocks) else smartSourceBlocks

            val entitiesToSave = mergedBlocks.map {
                SubtitleBlockEntity(
                    youtubeVideoId = videoId,
                    startTimeMs = it.startTimeMs,
                    endTimeMs = it.endTimeMs,
                    sourceText = it.sourceText,
                    translatedText = it.translatedText
                )
            }
            subtitleBox.put(entitiesToSave)

            val savedBlocks = subtitleBox.query(SubtitleBlockEntity_.youtubeVideoId.equal(videoId)).build().find()
                .map { SubtitleBlock(it.id, it.startTimeMs, it.endTimeMs, it.sourceText, it.translatedText, it.mlKitTranslatedText, it.onnxTranslatedText) }
                .sortedBy { it.startTimeMs }

            Result.success(
                VideoPlaybackInfo(
                    videoUrl = videoStream.url.toString(),
                    audioUrl = finalAudioUrl.toString(),
                    subtitles = savedBlocks,
                    hasYoutubeTranslation = ruBlocks.isNotEmpty(),
                    sourceLang = sourceLang,
                    availableQualities = availableQualities
                )
            )
        } catch (e: Exception) {
            Log.e(TAG, "❌ КРИТИЧЕСКАЯ ОШИБКА: ${e.message}")
            Result.failure(e)
        }
    }

    override suspend fun translateAndSaveBlockLocally(block: SubtitleBlock, sourceLang: String, mode: TranslationMode): SubtitleBlock = withContext(Dispatchers.IO) {
        val activeTranslator = if (mode == TranslationMode.ADVANCED_ONNX) onnxTranslator else mlKitTranslator
        val translation = activeTranslator.translate(block.sourceText, sourceLang, "ru").getOrNull()

        val updatedBlock = if (mode == TranslationMode.ADVANCED_ONNX) {
            block.copy(onnxTranslatedText = translation ?: "Ошибка перевода")
        } else {
            block.copy(mlKitTranslatedText = translation ?: "Ошибка перевода")
        }

        val entity = subtitleBox.get(block.id)
        if (entity != null) {
            if (mode == TranslationMode.ADVANCED_ONNX) entity.onnxTranslatedText = updatedBlock.onnxTranslatedText
            else entity.mlKitTranslatedText = updatedBlock.mlKitTranslatedText
            subtitleBox.put(entity)
        }
        updatedBlock
    }

