    private data class TimedWord(val text: String, val start: Long, val end: Long)

    private fun buildAestheticSubtitles(rawBlocks: List<SubtitleBlock>): List<SubtitleBlock> {
        if (rawBlocks.isEmpty()) return emptyList()

        val allWords = mutableListOf<TimedWord>()
        for (block in rawBlocks) {
            val words = block.sourceText.split(Regex("\\s+")).filter { it.isNotBlank() }
            if (words.isEmpty()) continue

            val duration = maxOf(1L, block.endTimeMs - block.startTimeMs)
            val timePerWord = duration / words.size

            words.forEachIndexed { i, word ->
                allWords.add(TimedWord(
                    text = word,
                    start = block.startTimeMs + (i * timePerWord),
                    end = block.startTimeMs + ((i + 1) * timePerWord)
                ))
            }
        }

        val logicalSentences = mutableListOf<List<TimedWord>>()
        var currentSentence = mutableListOf<TimedWord>()

        for (i in allWords.indices) {
            val word = allWords[i]
            currentSentence.add(word)

            val endsWithPunctuation = word.text.lastOrNull() in listOf('.', '!', '?')
            val isNextWordFar = if (i < allWords.size - 1) {
                (allWords[i + 1].start - word.end) > 1500L
            } else false

            val isTooLong = currentSentence.joinToString(" ") { it.text }.length > 150

            if (endsWithPunctuation || isNextWordFar || isTooLong) {
                logicalSentences.add(currentSentence)
                currentSentence = mutableListOf()
            }
        }
        if (currentSentence.isNotEmpty()) logicalSentences.add(currentSentence)

        var idCounter = 0L
        val finalBlocks = mutableListOf<SubtitleBlock>()

        for (sentence in logicalSentences) {
            val chunks = splitSentenceSmartly(sentence)
            for (chunk in chunks) {
                if (chunk.isEmpty()) continue
                finalBlocks.add(SubtitleBlock(
                    id = idCounter++,
                    startTimeMs = chunk.first().start,
                    endTimeMs = chunk.last().end,
                    sourceText = chunk.joinToString(" ") { it.text }
                ))
            }
        }

        return finalBlocks
    }

    // =======================================================================
    // СМАРТ-ЧАНКИНГ (СИНТАКСИЧЕСКОЕ РАЗБИЕНИЕ)
    // =======================================================================

    private fun splitSentenceSmartly(sentence: List<TimedWord>): List<List<TimedWord>> {
        val text = sentence.joinToString(" ") { it.text }

        if (text.length <= 85 || sentence.size <= 5) {
            return listOf(sentence)
        }

        var bestSplitIndex = -1
        var maxScore = Int.MIN_VALUE

        val conjunctions = setOf(
            "and", "but", "so", "because", "until", "while", "after", "before", "when", "which", "that", "who", "where", "if",
            "et", "ou", "mais", "donc", "car", "parce", "que", "qui", "quand", "si", "comme", "puisque"
        )
        val orphans = setOf(
            "a", "an", "the", "in", "on", "at", "to", "for", "of", "with", "by", "from", "is", "are", "was", "were", "am", "i", "you", "he", "she", "it", "we", "they", "my", "your", "his", "her", "our", "their",
            "le", "la", "les", "un", "une", "des", "de", "du", "d'", "l'", "à", "au", "aux", "dans", "sur", "en", "vers", "avec", "sans", "sous", "par", "pour", "je", "tu", "il", "elle", "on", "nous", "vous", "ils", "elles", "mon", "ton", "son", "ma", "ta", "sa", "mes", "tes", "ses", "est", "sont", "suis", "es", "c'", "c", "qu'", "qu"
        )

        for (i in 2 until sentence.size - 2) {
            var score = 0

            val wordBeforeRaw = sentence[i - 1].text
            val wordAfterRaw = sentence[i].text

            // Using static pre-compiled regex objects
            val wordBefore = wordBeforeRaw.lowercase().replace(UNWANTED_CHARACTERS_REGEX, "")
            val wordAfter = wordAfterRaw.lowercase().replace(UNWANTED_CHARACTERS_REGEX, "")

            if (wordBeforeRaw.endsWith(",")) score += 100
            if (wordBeforeRaw.endsWith(";") || wordBeforeRaw.endsWith("-")) score += 80
            if (wordAfter in conjunctions) score += 90

            if (wordBefore in orphans) score -= 500
            if (wordAfterRaw.matches(ENDS_WITH_PUNCTUATION_REGEX)) score -= 500

            val leftSize = i
            val rightSize = sentence.size - i

            if (leftSize < 3 || rightSize < 3) score -= 1000

            val diff = kotlin.math.abs(leftSize - rightSize)
            score -= (diff * 5)

            if (score > maxScore) {
                maxScore = score
                bestSplitIndex = i
            }
        }

        if (bestSplitIndex == -1 || maxScore <= -100) {
            bestSplitIndex = sentence.size / 2
        }

        val leftChunk = sentence.subList(0, bestSplitIndex)
        val rightChunk = sentence.subList(bestSplitIndex, sentence.size)

        val result = mutableListOf<List<TimedWord>>()
        result.addAll(splitSentenceSmartly(leftChunk))
        result.addAll(splitSentenceSmartly(rightChunk))

        return result
    }

    private fun mergeSubtitles(smartSourceBlocks: List<SubtitleBlock>, rawRuBlocks: List<SubtitleBlock>): List<SubtitleBlock> {
        if (smartSourceBlocks.isEmpty() || rawRuBlocks.isEmpty()) return smartSourceBlocks

        val ruWords = mutableListOf<TimedWord>()
        for (ru in rawRuBlocks) {
            val words = ru.sourceText.split(MULTIPLE_SPACES_REGEX).filter { it.isNotBlank() }
            if (words.isEmpty()) continue
            val duration = maxOf(1L, ru.endTimeMs - ru.startTimeMs)
            val timePerWord = duration / words.size
            words.forEachIndexed { i, w ->
                ruWords.add(TimedWord(w, ru.startTimeMs + (i * timePerWord), ru.startTimeMs + ((i + 1) * timePerWord)))
            }
        }

        val blockAllocations = Array(smartSourceBlocks.size) { mutableListOf<TimedWord>() }
        for (ruWord in ruWords) {
            val wordCenter = (ruWord.start + ruWord.end) / 2
            var bestIndex = -1
            var minDistance = Long.MAX_VALUE

            for (i in smartSourceBlocks.indices) {
                val smart = smartSourceBlocks[i]
                if (wordCenter in smart.startTimeMs..smart.endTimeMs) {
                    bestIndex = i
                    break
                } else {
                    val dist = minOf(kotlin.math.abs(wordCenter - smart.startTimeMs), kotlin.math.abs(wordCenter - smart.endTimeMs))
                    if (dist < minDistance) {
                        minDistance = dist
                        bestIndex = i
                    }
                }
            }
            if (bestIndex != -1) blockAllocations[bestIndex].add(ruWord)
        }

        val ruConjunctions = setOf("и", "а", "но", "да", "или", "либо", "что", "чтобы", "как", "если", "хотя", "где", "когда", "который", "которая", "потому", "поэтому", "так", "затем", "потом", "ведь", "чем")
        val ruDangling = setOf("в", "на", "с", "к", "от", "из", "у", "за", "над", "под", "о", "об", "для", "до", "без", "не", "ни")

        for (i in 0 until smartSourceBlocks.size - 1) {
            val currentWords = blockAllocations[i]
            val nextWords = blockAllocations[i + 1]

            if (currentWords.isEmpty() && nextWords.isEmpty()) continue

            val windowSize = 4
            val leftPart = currentWords.takeLast(windowSize)
            val rightPart = nextWords.take(windowSize)
            val window = leftPart + rightPart

            if (window.isEmpty()) continue

            val originalCutIndex = leftPart.size
            var bestCutIndex = originalCutIndex
            var maxScore = -1000

            for (k in 1 until window.size) {
                var score = 0
                val wordBefore = window[k - 1].text.lowercase()
                val wordAfter = window[k].text.lowercase()
                val cleanWordBefore = wordBefore.replace(RUSSIAN_PUNCTUATION_REGEX, "")
                val cleanWordAfter = wordAfter.replace(RUSSIAN_PUNCTUATION_REGEX, "")

                if (wordBefore.matches(SENTENCE_END_MATCH_REGEX)) score += 100
                if (cleanWordAfter in ruConjunctions) score += 50
                if (cleanWordBefore in ruDangling) score -= 200
                if (cleanWordAfter in ruDangling) score += 20

                val distance = kotlin.math.abs(k - originalCutIndex)
                score -= (distance * 15)

                if (score > maxScore) {
                    maxScore = score
                    bestCutIndex = k
                }
            }

            if (bestCutIndex != originalCutIndex && maxScore > -500) {
                val shift = bestCutIndex - originalCutIndex
                if (shift > 0) {
                    val wordsToMove = nextWords.take(shift)
                    currentWords.addAll(wordsToMove)
                    blockAllocations[i + 1] = nextWords.drop(shift).toMutableList()
                } else {
                    val absShift = kotlin.math.abs(shift)
                    val wordsToMove = currentWords.takeLast(absShift)
                    nextWords.addAll(0, wordsToMove)
                    blockAllocations[i] = currentWords.dropLast(absShift).toMutableList()
                }
            }
        }

        return smartSourceBlocks.mapIndexed { i, smart ->
            val mergedText = blockAllocations[i].joinToString(" ") { it.text }.takeIf { it.isNotBlank() }
            smart.copy(translatedText = mergedText)
        }
    }

    private fun getInnertubeCaptionInfo(videoId: String, targetLang: String? = null): Pair<String, String>? {
        try {
            val body = JSONObject().apply {
                put("videoId", videoId)
                put("context", JSONObject().apply {
                    put("client", JSONObject().apply {
                        put("clientName", "WEB")
                        put("clientVersion", "2.20250601.01.00")
                        put("hl", "en") // Важно использовать "en", чтобы не было багов с локализацией
                    })
                })
            }

            val request = Request(
                "POST",
                "https://www.youtube.com/youtubei/v1/player",
                mapOf(
                    "Content-Type" to listOf("application/json"),
                    "User-Agent" to listOf("Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36")
                ),
                body.toString().toByteArray(Charsets.UTF_8),
                null,
                false
            )

            val response = NewPipeDownloader.getInstance().execute(request)
            if (response.responseCode() in 200..299) {
                val json = JSONObject(response.responseBody())
                val tracks = json.optJSONObject("captions")
                    ?.optJSONObject("playerCaptionsTracklistRenderer")
                    ?.optJSONArray("captionTracks") ?: return null

                var bestTrack: JSONObject? = null

                if (targetLang != null) {
                    // Ищем ручные сабы для целевого языка
                    for (i in 0 until tracks.length()) {
                        val t = tracks.optJSONObject(i) ?: continue
                        val vssId = t.optString("vssId", "")
                        if (t.optString("languageCode").startsWith(targetLang, ignoreCase = true) && !vssId.startsWith("a.")) {
                            bestTrack = t
                            break
                        }
                    }
                    // Если нет ручных, берем любые (например, переведенные авто)
                    if (bestTrack == null) {
                        for (i in 0 until tracks.length()) {
                            val t = tracks.optJSONObject(i) ?: continue
                            if (t.optString("languageCode").startsWith(targetLang, ignoreCase = true)) {
                                bestTrack = t
                                break
                            }
                        }
                    }
                } else {
                    // ИЩЕМ ОРИГИНАЛЬНЫЙ ЯЗЫК АУДИО (ASR)
                    for (i in 0 until tracks.length()) {
                        val t = tracks.optJSONObject(i) ?: continue
                        val vssId = t.optString("vssId", "")
                        val kind = t.optString("kind", "")
                        // vssId начинается с "a." означает auto-generated (сгенерировано движком YouTube).
                        // Это 100% гарантия того, что это оригинальный язык речи.
                        if (kind == "asr" || vssId.startsWith("a.")) {
                            bestTrack = t
                            break
                        }
                    }
                }

                if (bestTrack != null) {
                    var url = bestTrack.optString("baseUrl")
                    val lang = bestTrack.optString("languageCode").substringBefore("-")

                    if (targetLang != null && !url.contains("tlang=")) {
                        url += if (url.contains("?")) "&tlang=$targetLang" else "?tlang=$targetLang"
                    }
                    url = url.replace(Regex("fmt=[^&]+"), "fmt=json3").let {
                        if (!it.contains("fmt=json3")) "$it&fmt=json3" else it
                    }
                    return Pair(url, lang)
                }
            }
        } catch (e: Exception) {
            e.printStackTrace()
        }
        return null
    }

